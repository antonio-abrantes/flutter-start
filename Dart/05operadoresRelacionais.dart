void main(List<String> args) {

  // Operadores de relacionais
  print('Operadores de relacionais');
  print(5 == 5);
  print(5 != 5);
  print(5 > 1);
  print(5 >= 5);
  print(5 < 1);
  print(5 <= 6);

  // Operadores lógicos
  print('Operadores lógicos');
  print( 5 >= 6 && 5 != 6);
  print( 5 >= 6 || 5 != 6);
  print( !(5 > 6));
  print( 5 >= 6 && 5 != 6);

}
